package br.com.cobrasin.tabela;

public class Municipio {
	
	private String Id,UF,IdProdesp,Cidade;

	public String getId() {
		return Id;
	}

	public void setId(String id) {
		Id = id;
	}

	public String getUF() {
		return UF;
	}

	public void setUF(String uF) {
		UF = uF;
	}

	public String getIdProdesp() {
		return IdProdesp;
	}

	public void setIdProdesp(String idProdesp) {
		IdProdesp = idProdesp;
	}

	public String getCidade() {
		return Cidade;
	}

	public void setCidade(String cidade) {
		Cidade = cidade;
	}

}
